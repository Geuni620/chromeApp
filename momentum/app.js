const calculater = {
  add: function (a, b) {
    console.log(a + b);
  },
  minus: function (a, b) {
    console.log(a - b);
  },
  muti: function (a, b) {
    console.log(a * b);
  },
  divide: function (a, b) {
    console.log(a / b);
  },
  power: function (a, b) {
    console.log(a ** b);
  },
};

calculater.add(10, 5);
calculater.minus(10, 5);
calculater.muti(10, 5);
calculater.divide(10, 5);
calculater.power(10, 5);
